package ex_04_Operators;

public class Lab36_37_Operators_Assignment_Arithmetic {
    public static void main(String[] args) {
        int a = 20;
        int b = 3;
        float c = 0.3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println("a+b");
    }
}
